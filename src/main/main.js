function applyAutoFocus() {
  if (autoFocusSnapshot) return
  autoFocusSnapshot = { blocker: blocker.state.enabled, focusMode: focusMode.state.enabled }
  blocker.setEnabled(true)
  focusMode.setEnabled(true)
}
function revertAutoFocus() {
  if (!autoFocusSnapshot) return
  blocker.setEnabled(autoFocusSnapshot.blocker)
  focusMode.setEnabled(autoFocusSnapshot.focusMode)
  autoFocusSnapshot = null
}

