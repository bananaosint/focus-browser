// ---- native popup menus ----
//
// The chrome UI's tab strip/toolbar live in a WebContentsView that's only
// CHROME_HEIGHT (76px) tall — its content is physically clipped to that
// rectangle, `position: fixed` and z-index included, the same way an
// iframe's content can't render outside its box. A custom HTML dropdown
// taller than that (a launcher list, a multi-item context menu) renders but
// is invisible past the 76px line. Native menus aren't web content, so
// they aren't bound by any view's rectangle — that's why both of these use
// Menu.popup() instead of an in-page element. Both take the resolved window
// context explicitly (rather than reaching for the bare primary tabManager)
// so they act on whichever window actually sent the request.

function showTabContextMenu(ctx, tabId, x, y) {
  const state = ctx.tabManager.getState()
  const tab = state.tabs.find((t) => t.id === tabId)
  if (!tab) return

  const template = [
    {
      label: 'New group from tab',
      click: () => {
        const groupId = ctx.tabManager.createGroup()
        ctx.tabManager.addTabToGroup(tabId, groupId)
      }
    }
  ]

  if (tab.groupId) {
    template.push({ label: 'Remove from group', click: () => ctx.tabManager.removeTabFromGroup(tabId) })
    template.push({ label: 'Close group', click: () => ctx.tabManager.closeGroup(tab.groupId) })
  } else if (state.groups.length) {
    state.groups.forEach((g) => {
      template.push({ label: `Add to "${g.name}"`, click: () => ctx.tabManager.addTabToGroup(tabId, g.id) })
    })
  }

  if (tabId !== ctx.tabManager.activeTabId && !tab.frozen) {
    template.push({ type: 'separator' })
    template.push({ label: 'Freeze tab (free its memory)', click: () => ctx.tabManager.freezeTab(tabId) })
  }

  template.push({ type: 'separator' })
  template.push({
    label: tab.isMuted ? 'Unmute tab' : 'Mute tab',
    click: () => ctx.tabManager.toggleMute(tabId)
  })
  template.push({
    label: 'Print...',
    click: () => {
      const t = ctx.tabManager.tabs.get(tabId)
      if (t && t.view) t.view.webContents.print({ silent: false, printBackground: true })
    }
  })

  template.push({ type: 'separator' })
  template.push({ label: 'Close tab', click: () => ctx.tabManager.closeTab(tabId) })

  Menu.buildFromTemplate(template).popup({ window: ctx.win, x: Math.round(x), y: Math.round(y) })
}

function showNewTabContextMenu(ctx, x, y) {
  const template = [
    { label: 'New Tab', click: () => ctx.tabManager.createTab(NEW_TAB_URL) },
    { label: 'New Incognito Tab', click: () => ctx.tabManager.createTab(NEW_TAB_URL, { incognito: true }) }
  ]
  Menu.buildFromTemplate(template).popup({ window: ctx.win, x: Math.round(x), y: Math.round(y) })
}

function showLauncherMenu(ctx, x, y) {
  const list = profiles.state.list
  const template = []

  if (!list.length) {
    template.push({ label: 'No profiles yet', enabled: false })
  } else {
    list.forEach((profile) => {
      template.push({
        label: profile.name,
        click: () => {
          profiles.launchProfile(profile.id, { onOpenUrl: (url) => ctx.tabManager.createTab(resolveInput(url)) })
        }
      })
    })
  }

  template.push({ type: 'separator' })
  template.push({ label: 'Manage profiles…', click: () => openSettingsWindow() })

  Menu.buildFromTemplate(template).popup({ window: ctx.win, x: Math.round(x), y: Math.round(y) })
}

