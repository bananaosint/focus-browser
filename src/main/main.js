// Once a minute, while an actual Pomodoro work session is running (not
// paused, not on break), snapshot the primary window's active tab into the
// Activity Log — a lightweight "what was I doing" trail tied to real focus
// sessions rather than all browsing. Skips incognito tabs and blank/new-tab
// pages, same privacy carve-out as History/UsageStats.
setInterval(() => {
  if (!pomodoro.running || pomodoro.phase !== 'work') return
  if (!tabManager) return
  const tab = tabManager.tabs.get(tabManager.activeTabId)
  if (!tab || tab.isIncognito) return
  if (!tab.url || tab.url === NEW_TAB_URL || tab.url.startsWith('file://')) return
  activityLog.record({ url: tab.url, title: tab.title })
}, 60_000)

// Every 5 minutes, while a work session is running AND the user has opted
// into this in Settings → AI Chat, send just THIS session's Activity Log
// (workSessionStartAt onward — never a prior session's, never break-time
// browsing, since the ticker above only ever records during 'work') to the
// AI and let it judge whether things look on-track. The only action it's
// allowed to take is a passive nudge (toast + desktop notification) — it
// cannot block sites, end the session, or otherwise act on its own here.
setInterval(async () => {
  if (!store.get('aiChat').productivityMonitorEnabled) return
  if (!pomodoro.running || pomodoro.phase !== 'work' || !workSessionStartAt) return

  const entries = activityLog.getRecent(500).filter((e) => e.timestamp >= workSessionStartAt)
  if (!entries.length) return

  const result = await aiChat.checkProductivity({ entries })
  if (result.checked && result.offTrack && result.message) {
    chromeView?.webContents.send('toast:show', `🎯 ${result.message}`)
    if (Notification.isSupported()) {
      // timeoutType: 'never' — the default toast duration is too short to
      // actually read a 1-2 sentence nudge; this keeps it up until the user
      // dismisses it themselves (Windows/Linux; macOS has no equivalent and
      // just falls back to its own default, harmlessly ignoring the option).
      new Notification({ title: 'Focus check-in', body: result.message, timeoutType: 'never' }).show()
    }
    const nudge = { message: result.message, timestamp: Date.now() }
    store.update('productivityNudge', nudge)
    dashboardWin?.webContents.send('productivityNudge:state', nudge)
  }
}, 5 * 60_000)

// Snapshot of blocker/focusMode.enabled taken right before a Pomodoro work
