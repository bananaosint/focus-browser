function zoomActiveTab(webContents, direction) {
  const ctx = contextForSender(webContents)
  if (!ctx) return
  const activeId = ctx.tabManager.activeTabId
  const tab = ctx.tabManager.tabs.get(activeId)
  if (!tab || !tab.view) return
  const wc = tab.view.webContents
  
  let factor = wc.getZoomFactor()
  if (direction === 1) {
    factor = Math.min(3.0, factor + 0.1)
  } else if (direction === -1) {
    factor = Math.max(0.3, factor - 0.1)
  } else {
    factor = 1.0
  }
  
  wc.setZoomFactor(factor)
  
  try {
    const url = new URL(tab.url)
    const host = url.hostname.toLowerCase()
    const zoomSettings = store.get('zoomSettings') || { hosts: {} }
    if (!zoomSettings.hosts) zoomSettings.hosts = {}
    
    if (factor === 1.0) {
      delete zoomSettings.hosts[host]
    } else {
      zoomSettings.hosts[host] = factor
    }
    
    store.update('zoomSettings', { hosts: zoomSettings.hosts })
    ctx.chromeView.webContents.send('tab:zoomChanged', { tabId: tab.id, zoomFactor: factor })
  } catch (err) {
    // ignore local files
  }
}

// ---- keyboard shortcuts ----
//
// Attached via before-input-event to every webContents the browser creates
// (chrome, both primary-only sidebars, and every tab in every window — see
// attachShortcutHandler in tabManager.js) rather than Electron menu
// accelerators, since the app menu is stripped entirely
// (Menu.setApplicationMenu(null)). Returning true tells the caller to
// preventDefault, which matters for things like Ctrl+W: without it, the
// keystroke would also reach whatever page or input field has focus.
function handleGlobalShortcut(input, webContents) {
  const isCmdOrCtrl = process.platform === 'darwin' ? input.meta : input.control
  const isOtherModifier = process.platform === 'darwin' ? input.control || input.alt : input.meta || input.alt
  if (input.type !== 'keyDown' || !isCmdOrCtrl || isOtherModifier) return false
  const key = input.key.toLowerCase()

  if (key === 'n' && !input.shift) {
    createSecondaryWindow()
    return true
  }

  if (key === 'n' && input.shift) {
    createIncognitoWindow()
    return true
  }

  if (key === '=' || key === '+') {
    zoomActiveTab(webContents, 1)
    return true
  }
  if (key === '-') {
    zoomActiveTab(webContents, -1)
    return true
  }
  if (key === '0') {
    zoomActiveTab(webContents, 0)
    return true
  }
  if (key === 'p') {
    const ctx = contextForSender(webContents)
    if (ctx) {
      const activeId = ctx.tabManager.activeTabId
      const tab = ctx.tabManager.tabs.get(activeId)
      if (tab && tab.view) {
        tab.view.webContents.print({ silent: false, printBackground: true })
      }
    }
    return true
  }
  if (key === 'f') {
    const ctx = contextForSender(webContents)
    if (ctx) {
      ctx.chromeView.webContents.send('tab:findInPageToggle')
    }
    return true
  }

  const ctx = contextForSender(webContents)
  if (!ctx) return false
  const tm = ctx.tabManager

  if (key === 't' && !input.shift) {
    tm.createTab()
    return true
  }
  if (key === 't' && input.shift) {
    tm.reopenClosedTab()
    return true
  }
  if (key === 'w' && !input.shift) {
    if (tm.activeTabId) tm.closeTab(tm.activeTabId)
    return true
  }
  if (key === 'tab') {
    tm.switchToNextTab()
    return true
  }
  if (/^[1-8]$/.test(key)) {
    tm.switchToTabIndex(Number(key) - 1)
    return true
  }
  return false
}

// ---- workspace sidebar drag-to-resize ----
//
// Tracking uses screen.getCursorScreenPoint() polled from the main process,
// not renderer mousemove — a renderer's DOM events stop once the cursor
// leaves that WebContentsView's bounds, and a resize drag needs to keep
// tracking even as the boundary itself moves under the cursor. Polling the
// OS cursor position directly sidesteps that entirely: it doesn't care
// which view (if any) the cursor is currently hit-testing into. Primary-
// window-only, same as the workspace sidebar itself.
//
// The corresponding mouseup listener still lives in the renderer (see
// workspace.js) and relies on the view's bounds being kept just past the
// cursor on every tick (RESIZE_EDGE_BUFFER) so that release reliably lands
// inside the workspace view's hit region rather than the tab view beyond it.
const RESIZE_EDGE_BUFFER = 8
let resizeInterval = null
let resizeTimeout = null

function startSidebarResize() {
  stopSidebarResize()
  if (!win || !tabManager?.sidebarVisible) return
  resizeInterval = setInterval(() => {
    if (!win) return
    const contentBounds = win.getContentBounds()
    const cursor = screen.getCursorScreenPoint()
    const relativeX = cursor.x - contentBounds.x + RESIZE_EDGE_BUFFER
    const clamped = Math.min(MAX_SIDEBAR_WIDTH, Math.max(MIN_SIDEBAR_WIDTH, relativeX))
    tabManager.setSidebarWidth(clamped) // live visual only — no disk write per tick
  }, 16)
  // A drag that never gets a clean end signal (cursor released somewhere
  // this app has no listener, e.g. off-screen) shouldn't spin forever.
  resizeTimeout = setTimeout(stopSidebarResize, 15000)
}

function stopSidebarResize() {
  if (resizeTimeout) {
    clearTimeout(resizeTimeout)
    resizeTimeout = null
  }
  if (!resizeInterval) return
  clearInterval(resizeInterval)
  resizeInterval = null
  if (tabManager) workspace.setSidebarWidth(tabManager.sidebarWidth) // persist the final width once
}

function registerUnlockProtocol() {
  session.defaultSession.protocol.handle('focus-action', async (request) => {
    const url = new URL(request.url)
    if (url.hostname === 'unlock') {
      const host = url.searchParams.get('host')
      const target = url.searchParams.get('target')
      const minutes = Number(url.searchParams.get('minutes')) || 10
      console.log('=== UNLOCK PROTOCOL CALLED ===', { host, target, minutes })
      if (host && target) blocker.temporarilyUnlock(host, minutes)
      console.log('=== CURRENT ACTIVE UNLOCKS ===', Array.from(blocker.temporaryUnlocks.entries()))
      return new Response(null, { status: 302, headers: { Location: target || NEW_TAB_URL } })
    }

    if (url.hostname === 'evaluate') {
      const host = url.searchParams.get('host') || ''
      const justification = url.searchParams.get('justification') || ''
      try {
        const result = await aiChat.evaluateJustification({ host, justification })
        return new Response(JSON.stringify(result), {
          headers: { 
            'Content-Type': 'application/json',
            'Access-Control-Allow-Origin': '*'
          }
        })
      } catch (err) {
        return new Response(JSON.stringify({ approved: true, reason: 'AI Gatekeeper bypass: ' + err.message }), {
          headers: { 
            'Content-Type': 'application/json',
            'Access-Control-Allow-Origin': '*'
          }
        })
      }
    }

    return new Response('Not found', { status: 404 })
  })
}

