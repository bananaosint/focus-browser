// ---- window contexts (multi-window support, Ctrl+N) ----
//
// Every open browser window (the primary one, plus any Ctrl+N secondary
// windows) gets a { win, chromeView, tabManager, isPrimary } entry here.
// IPC handlers that act on "the tab manager" resolve which window sent the
// request via contextForSender(event.sender) instead of assuming a single
// global tabManager — that assumption held fine when only one window could
// ever exist, but silently breaks the first window the moment a second one
// is created (reassigning module-level win/chromeView/tabManager would
// orphan whichever window they used to point at).
//
// Scoped deliberately: secondary windows get tabs/chrome only. The
// workspace sidebar and AI chat sidebar stay exclusive to the primary
// window (win/chromeView/workspaceView/aiSidebarView/tabManager below) —
// giving every window its own independent open folder or chat sidebar
// would mean real product decisions (does opening a folder in one window
// show it in both? a shared chat history or separate ones?) that aren't
// this pass's to make. Settings/Pomodoro/Dashboard are unaffected either
// way — they were already single global utility windows, not per-browser-
// window state.
const windows = []

function primaryContext() {
  return windows.find((ctx) => ctx.isPrimary)
}

// Resolves a window context from *any* webContents that could send a
// request — an IPC message from the chrome view or a primary-only sidebar,
// or a keyboard shortcut from an arbitrary tab (tabs have no preload/IPC, so
// before-input-event is the only signal that can originate there; see
// attachShortcutHandler in tabManager.js). Used for both event.sender (IPC)
// and the webContents passed to keyboard shortcut handlers.
function contextForSender(webContents) {
  const primary = primaryContext()
  if (primary) {
    if (workspaceView && webContents.id === workspaceView.webContents.id) return primary
    if (aiSidebarView && webContents.id === aiSidebarView.webContents.id) return primary
  }
  return windows.find((ctx) => {
    if (ctx.chromeView.webContents.id === webContents.id) return true
    for (const tab of ctx.tabManager.tabs.values()) {
      if (tab.view && tab.view.webContents.id === webContents.id) return true
    }
    return false
  })
}

function broadcastToAllChrome(channel, payload) {
  windows.forEach((ctx) => {
    if (!ctx.chromeView.webContents.isDestroyed()) ctx.chromeView.webContents.send(channel, payload)
  })
}

// Every trusted renderer (chrome for every window, both primary sidebars, and
// each satellite utility window) that should restyle itself when the theme
// changes. Sandboxed tab pages (newtab/blocked) are handled separately via the
// written file + executeJavaScript push, not this list.
function trustedThemeTargets() {
  const targets = []
  windows.forEach((ctx) => targets.push(ctx.chromeView.webContents))
  if (workspaceView) targets.push(workspaceView.webContents)
  if (aiSidebarView) targets.push(aiSidebarView.webContents)
  ;[settingsWin, pomodoroWin, dashboardWin, onboardingWin].forEach((w) => {
    if (w && !w.isDestroyed()) targets.push(w.webContents)
  })
  return targets
}

// Live-update any open newtab/blocked tab. The page defines window.__applyFbTheme
// (see newtab.js / blocked.js); if it isn't defined yet the page is still
// loading and will read the freshly-written theme-state.js on its own.
function pushThemeToTabs(resolved) {
  const js = `window.__applyFbTheme && window.__applyFbTheme(${JSON.stringify(resolved.palette)}, ${JSON.stringify(resolved.mode)});`
  windows.forEach((ctx) => {
    for (const tab of ctx.tabManager.tabs.values()) {
      if (!tab.view || tab.view.webContents.isDestroyed()) continue
      const url = tab.url || ''
      if (url.startsWith(NEW_TAB_URL) || url.startsWith(BLOCKED_TAB_URL)) {
        tab.view.webContents.executeJavaScript(js).catch(() => {})
      }
    }
  })
}

// The single fan-out for a theme change: persist-driven resolved value in,
// every surface updated out.
function applyThemeEverywhere(resolved) {
  writeThemeJs(resolved)
  trustedThemeTargets().forEach((wc) => {
    if (!wc.isDestroyed()) wc.send('theme:changed', resolved)
  })
  pushThemeToTabs(resolved)
}

